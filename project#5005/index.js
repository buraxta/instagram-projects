let x = 5
console.log(x++)
console.log(++x)
console.log(x)

/*
let x = 5: This line creates a variable 
named x and assigns the value 5 to it.

console.log(x++): This line prints the current 
value of x (which is 5) and then increments x 
by 1. However, the increment happens after the
value is printed.

console.log(++x): This line first increments x 
by 1 (which is now 7 after the previous line). 
Then, it prints the new value of x (which is 7).

console.log(x): This line simply prints the 
current value of x (which is still 7 from the 
previous line).
*/